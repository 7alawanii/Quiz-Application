    private void btn_finActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_finActionPerformed
        Quiz.answer = txt_ans.getText();
        if (Quiz.answer == null) {
            txt_ques.setText(null);
            Quiz.que = Quiz.q.get(Quiz.i);
            txt_ques.setText(Quiz.que.get_Question());
            Quiz.i++;
        } else {
            if (Quiz.answer.equalsIgnoreCase("true")) {
                Quiz.bans = true;
                Quiz.ca = Quiz.que.check_Answer(Quiz.bans);
            } else if (Quiz.answer.equalsIgnoreCase("false")) {
                Quiz.bans = false;
                Quiz.ca = Quiz.que.check_Answer(Quiz.bans);
            } else {
                Quiz.ca = Quiz.que.check_Answer(Quiz.answer);
            }
            if (Quiz.ca == true) {
                Quiz.score++;
            }
        //if (Quiz.flag >= 1) {
          //  Quiz.size -= Quiz.oldsize;
        //}
        Quiz.score1 = (((float) Quiz.score / (float) Quiz.size) * (float) 100);
        Quiz.score1 = Math.round(Quiz.score1 * 100) / (float) 100;
        if (Quiz.score1 >= 50.0) {
            Quiz.flag += 1;
            JOptionPane.showMessageDialog(null, "You Have Finished The Quiz.\nYou Have Answered " + Quiz.score + " Questions Right Out Of " + Quiz.size + " Questions.\nYour Final Score Is " + Quiz.score1 + " %\nYou Have Passed The Quiz.");
        } else {
            Quiz.flag += 1;
            JOptionPane.showMessageDialog(null, "You Have Finished The Quiz.\nYou Have Answered " + Quiz.score + " Questions Right Out Of " + Quiz.size + " Questions.\nYour Final Score Is " + Quiz.score1 + " %\nYou Have Failed The Quiz.");
        }
        try {
            String url = "jdbc:mysql://localhost:3306/quiz";
            String username = "root";
            String password = "root";
            Connection con1 = DriverManager.getConnection(url, username, password);
            Statement stmt1 = con1.createStatement();
            String Query1 = "UPDATE student SET score = '" + Quiz.score1 + "' WHERE score = '" + Quiz.res + "'";
            stmt1.execute(Query1);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
        Quiz.score = 0;
        Quiz.score1 = 0;
       //Quiz.oldsize = Quiz.size;
        //this.setVisible(false);
        //new Quiz_GUI().setVisible(true);
        System.exit(0);
        }
    }//GEN-LAST:event_btn_finActionPerformed

    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        txt_ans.requestFocusInWindow();
        Quiz.answer = txt_ans.getText();
        if (Quiz.answer == null) {
            txt_ques.setText(null);
            Quiz.que = Quiz.q.get(Quiz.i);
            txt_ques.setText(Quiz.que.get_Question());
            Quiz.i++;
        } else {
            if (Quiz.answer.equalsIgnoreCase("true")) {
                Quiz.bans = true;
                Quiz.ca = Quiz.que.check_Answer(Quiz.bans);
            } else if (Quiz.answer.equalsIgnoreCase("false")) {
                Quiz.bans = false;
                Quiz.ca = Quiz.que.check_Answer(Quiz.bans);
            } else {
                Quiz.ca = Quiz.que.check_Answer(Quiz.answer);
            }
            if (Quiz.ca == true) {
                Quiz.score++;
            }
            txt_ques.setText(null);
            txt_ans.setText(null);
            Quiz.que = Quiz.q.get(Quiz.i);
            txt_ques.setText(Quiz.que.get_Question());
            Quiz.i++;
            if (Quiz.i == Quiz.size) {
                btn_submit.setEnabled(false);
                btn_fin.setEnabled(true);
            }
        }
    }//GEN-LAST:event_btn_submitActionPerformed

