    final void FillLinkedList()
    {
        try {
            String url = "jdbc:mysql://localhost:3306/quiz";
            String username = "root";
            String password = "root";
            Connection con = DriverManager.getConnection(url, username, password);
            Statement stmt = con.createStatement();
            String Query = "SELECT * FROM questions";
            ResultSet rs = stmt.executeQuery(Query);

            while (rs.next()) {
                if (rs.getString(1).equalsIgnoreCase("fill in the blank")) {
                    Quiz.q.add(new FillInBlankQuestion(rs.getString(2), rs.getString(3)));
                } else if (rs.getString(1).equalsIgnoreCase("short answer")) {
                    Quiz.q.add(new ShortAnswerQuestion(rs.getString(2), rs.getString(3)));
                } else if (rs.getString(1).equalsIgnoreCase("true or false")) {
                    if (rs.getString(3).equalsIgnoreCase("true")) {
                        Quiz.q.add(new TrueFalseQuestion(rs.getString(2), true));
                    } else if (rs.getString(3).equalsIgnoreCase("false")) {
                        Quiz.q.add(new TrueFalseQuestion(rs.getString(2), false));
                    }
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }
